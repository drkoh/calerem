	/**
	 * @return the v_name
	 */
	public String getV_name();

	/**
	 * @param v_name the v_name to set
	 */
	public void setV_name(String v_name);

	/**
	 * @return the v_lastname
	 */
	public String getV_lastname();

	/**
	 * @param v_lastname the v_lastname to set
	 */
	public void setV_lastname(String v_lastname);

	/**
	 * @return the v_phone
	 */
	public long getV_phone();

	/**
	 * @param v_phone the v_phone to set
	 */
	public void setV_phone(long v_phone);

	/**
	 * @return the v_email
	 */
	public String getV_email();

	/**
	 * @param v_email the v_email to set
	 */
	public void setV_email(String v_email);

